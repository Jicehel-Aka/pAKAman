// === Fonctions utilitaires ===
static void draw_disc(uint16_t* sprite, int cx, int cy, int r, uint16_t color) {
    for (int y = 0; y < 16; ++y) {
        for (int x = 0; x < 16; ++x) {
            int dx = x - cx, dy = y - cy;
            if (dx*dx + dy*dy <= r*r) {
                sprite[y*16 + x] = color;
            }
        }
    }
}

// --- Helpers 14x14 pour les fruits (fond noir 0x0000 = transparent) ---
static inline void px14(uint16_t* s, int x, int y, uint16_t c) {
    if ((unsigned)x < 14u && (unsigned)y < 14u) s[y*14 + x] = c;
}
static void disc14(uint16_t* s, int cx, int cy, int r, uint16_t color) {
    for (int y = 0; y < 14; ++y)
        for (int x = 0; x < 14; ++x) {
            int dx = x - cx, dy = y - cy;
            if (dx*dx + dy*dy <= r*r) s[y*14 + x] = color;
        }
}
static void vline14(uint16_t* s, int x, int y0, int y1, uint16_t c) {
    for (int y = y0; y <= y1; ++y) px14(s, x, y, c);
}

// Genere les 8 sprites de fruits (formes simples mais reconnaissables).
static void build_fruit_sprites() {
    // Cerise : deux boules rouges + tige verte.
    for (int i = 0; i < 14*14; ++i) fruit_cherry[i] = COLOR_BLACK;
    disc14(fruit_cherry, 5, 10, 3, COLOR_RED);
    disc14(fruit_cherry, 9, 11, 2, COLOR_RED);
    px14(fruit_cherry, 5, 9, COLOR_WHITE);                 // reflet
    vline14(fruit_cherry, 6, 3, 9, COLOR_GREEN);           // tige
    vline14(fruit_cherry, 9, 4, 10, COLOR_GREEN);
    px14(fruit_cherry, 7, 3, COLOR_GREEN);

    // Fraise : corps rouge triangulaire + calice vert + pepins.
    for (int i = 0; i < 14*14; ++i) fruit_strawberry[i] = COLOR_BLACK;
    disc14(fruit_strawberry, 7, 8, 5, COLOR_RED);
    for (int x = 3; x <= 11; ++x) px14(fruit_strawberry, x, 12, COLOR_BLACK); // pointe
    px14(fruit_strawberry, 7, 13, COLOR_RED);
    for (int x = 4; x <= 10; ++x) px14(fruit_strawberry, x, 3, COLOR_GREEN);  // calice
    px14(fruit_strawberry, 7, 2, COLOR_GREEN);
    px14(fruit_strawberry, 5, 7, COLOR_YELLOW); px14(fruit_strawberry, 9, 7, COLOR_YELLOW);
    px14(fruit_strawberry, 7, 9, COLOR_YELLOW);

    // Orange : boule orange + petite feuille verte.
    for (int i = 0; i < 14*14; ++i) fruit_orange[i] = COLOR_BLACK;
    disc14(fruit_orange, 7, 8, 5, COLOR_ORANGE);
    px14(fruit_orange, 7, 2, COLOR_GREEN); px14(fruit_orange, 8, 2, COLOR_GREEN);

    // Pomme : boule rouge + creux + tige brune + feuille verte.
    for (int i = 0; i < 14*14; ++i) fruit_apple[i] = COLOR_BLACK;
    disc14(fruit_apple, 7, 8, 5, COLOR_RED);
    px14(fruit_apple, 7, 3, COLOR_BLACK); px14(fruit_apple, 6, 3, COLOR_BLACK);
    vline14(fruit_apple, 7, 2, 4, COLOR_BROWN);            // tige
    px14(fruit_apple, 9, 3, COLOR_GREEN); px14(fruit_apple, 10, 3, COLOR_GREEN);

    // Melon (pasteque) : boule vert clair + rayures.
    for (int i = 0; i < 14*14; ++i) fruit_melon[i] = COLOR_BLACK;
    disc14(fruit_melon, 7, 7, 6, COLOR_LIGHTGREEN);
    vline14(fruit_melon, 4, 3, 11, COLOR_GREEN);
    vline14(fruit_melon, 7, 2, 12, COLOR_GREEN);
    vline14(fruit_melon, 10, 3, 11, COLOR_GREEN);

    // Galaxian : petit vaisseau bleu/jaune.
    for (int i = 0; i < 14*14; ++i) fruit_galaxian[i] = COLOR_BLACK;
    for (int x = 4; x <= 9; ++x) px14(fruit_galaxian, x, 8, COLOR_BLUE);   // ailes
    px14(fruit_galaxian, 3, 9, COLOR_BLUE); px14(fruit_galaxian, 10, 9, COLOR_BLUE);
    vline14(fruit_galaxian, 6, 3, 9, COLOR_YELLOW);        // corps
    vline14(fruit_galaxian, 7, 3, 9, COLOR_YELLOW);
    px14(fruit_galaxian, 6, 2, COLOR_WHITE); px14(fruit_galaxian, 7, 2, COLOR_WHITE);

    // Cloche : corps jaune + battant.
    for (int i = 0; i < 14*14; ++i) fruit_bell[i] = COLOR_BLACK;
    for (int y = 3; y <= 10; ++y) {
        int half = 1 + (y - 3);
        if (half > 5) half = 5;
        for (int x = 7 - half; x <= 7 + half; ++x) px14(fruit_bell, x, y, COLOR_YELLOW);
    }
    for (int x = 2; x <= 12; ++x) px14(fruit_bell, x, 11, COLOR_YELLOW);  // socle
    px14(fruit_bell, 7, 12, COLOR_WHITE);                                  // battant
    px14(fruit_bell, 7, 2,  COLOR_GRAY);                                   // anneau

    // Cle : anneau + tige + dents (gris/or).
    for (int i = 0; i < 14*14; ++i) fruit_key[i] = COLOR_BLACK;
    disc14(fruit_key, 7, 4, 3, COLOR_SILVER);
    disc14(fruit_key, 7, 4, 1, COLOR_BLACK);              // trou de l'anneau
    vline14(fruit_key, 7, 6, 12, COLOR_SILVER);           // tige
    px14(fruit_key, 8, 10, COLOR_SILVER); px14(fruit_key, 8, 12, COLOR_SILVER); // dents
}

